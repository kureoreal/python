# Operadores Aritméticos
# +
suma = 5 + 5
print(suma)
# -
resta = 5 - 5
print(resta)
# *
multiplicar = 5 * 5
print(multiplicar)
# /
dividir = 5/4
print(dividir)
# // división entera
dividirInt = 5//4
print(dividirInt)
# **
exponente = 2**3
print(exponente)
# %
modulo = 5%2
print(modulo)

