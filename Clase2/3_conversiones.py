# Conversión implicita
numero1 = 12
numero2 = 2.0
print(type(numero1))
print(type(numero2))
resultado = numero1 + numero2
print(resultado)
print(type(resultado))

# Conversión explicita
numero3= 1.2
print(type(numero3))
print(numero3)
numero4 = int(numero3)
print(type(numero4))
print(numero4)
numero5 = float(numero4)
print(type(numero5))
print(numero5)
