# D = Defiente Nota = 0
nota = input("Escriba la letra de la nota: ")
match nota:
    case "D":
        print("Deficiente la nota es: 1")
    case "I":
        print("Insuficiente la nota es: 2")
    case "A":
        print("Aceptable la nota es: 3")
    case "S":
        print("Sobresaliente la nota es: 4")
    case "E":
        print("Exelente la nota es: 3")